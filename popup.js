function getLickUrl(locale, version, url) {
  let apiPath = url.slice(url.indexOf('docs/api/') + 9)
  let ver = parseInt(version);
  let modules = '';
  let pathValues = apiPath.split("/");
  const nowVersion = getNowVersion(url);
  if (ver > 10) {
    var key = "";
    if (nowVersion > 10){
      key = pathValues.slice(1,pathValues.length - 1).join("/")
    }else{
      key = pathValues.slice(0,pathValues.length - 1).join("/")
    }
    modules = obj[key] + "/";
  }
  if (nowVersion > 10){
    apiPath = pathValues.slice(1).join("/")
  }

  let targetLocale = '';
  if (locale == 'ja') {
    targetLocale = 'jp/';
  }
  return `https://docs.oracle.com/javase/${targetLocale}${version}/docs/api/${modules}${apiPath}`
}

function getNowVersion(nowUrl){
  let path = nowUrl.replace("https://docs.oracle.com/javase/","");
  let pathValues = path.split("/");
  for (var i = 0; i< pathValues.length;i++){
    try{
      const version = parseInt(pathValues[i])
      if(isNaN(version)){
        continue;
      }
      return version;
    }catch(e){
      continue;
    }
  }
  return 0;
}
